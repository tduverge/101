int		ft_atoi(char *str)
{
	int i;
	int neg;
	int nb;

	neg = 1;
	nb = 0;
	i = 0;
	while (((str[i] >= 9) && (str[i] <= 13)) || (str[i] == 32))
		i++;
	if (str[i] == '-')
	{
		neg = -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while ((str[i] <= '9') && (str[i] >= '0'))
	{
		nb = nb * 10 + str[i] - '0';
		i++;
	}
	return (nb * neg);
}
