char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int i;
	unsigned int src_len;

	src_len = 0;
	while (src[src_len] != '\0')
		src_len++;
	i = 0;
	while ((i < n) && (i <= src_len))
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}
