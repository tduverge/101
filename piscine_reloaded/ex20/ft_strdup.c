#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		i;
	char	*s2;

	i = 0;
	while (src[i])
		i++;
	if (!(s2 = malloc(sizeof(char) * i)))
		return (NULL);
	i = 0;
	while (src[i])
	{
		s2[i] = src[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
