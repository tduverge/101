void	delete_s1(int x, int y, int *sol)
{
	sol[x] -= 6 << (y - 1);
	sol[x + 1] -= 3 << (y - 1);
}

void	delete_s2(int x, int y, int *sol)
{
	sol[x] -= 1 << y;
	sol[x + 1] -= 3 << y;
	sol[x + 2] -= 2 << y;
}
