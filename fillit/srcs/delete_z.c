void		delete_z1(int x, int y, int *sol)
{
	sol[x] -= 3 << y;
	sol[x + 1] -= 6 << y;
}

void		delete_z2(int x, int y, int *sol)
{
	sol[x] -= 2 << (y - 1);
	sol[x + 1] -= 3 << (y - 1);
	sol[x + 2] -= 1 << (y - 1);
}
