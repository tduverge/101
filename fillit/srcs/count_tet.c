#include "../header/libft.h"

int		count_tet(char *s)
{
	int		len;

	len = ft_strlen(s);
	if (len == 20)
		return (1);
	else if ((len - 20) % 21 == 0)
		return ((len - 20) / 21 + 1);
	else
		return (0);
}
