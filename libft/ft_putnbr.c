#include "libft.h"

void	ft_putnbr(int n)
{
	long nl;

	nl = n;
	if (nl < 0)
	{
		ft_putchar('-');
		nl = -nl;
	}
	if (nl >= 10)
		ft_putnbr((int)(nl / 10));
	ft_putchar(nl % 10 + '0');
}
